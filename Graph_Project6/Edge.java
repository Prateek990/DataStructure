public class Edge {
	private int source;
	private int destination;
	private int dist;
	/**
	 * This method is to set source
	 */
	public int getSource() {
		return source;
	}
	/**
	 * This method is to set source 
	 */
	public void setSource(int source) {
		this.source = source;
	}
	/**
	 * This method is to get destination 
	 */
	public int getDestination() {
		return destination;
	}
	/**
	 * This method is to set destination
	 */
	public void setDestination(int dest) {
		this.destination = dest;
	}
	/**
	 * This method is to get distance 
	 */
	public int getDistance() {
		return dist;
	}
	/**
	 * This method is to set distance 
	 */
	public void setDistance(int distance) {
		this.dist = distance;
	}
	

}
